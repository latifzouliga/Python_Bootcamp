"""
6. Create a python file named KgToPounds, write a program that can convert kg to pounds
            Hints: 1 kg = 2.2 pounds

            Ex:
                Given data:
                    kg = 100

                output:
                    100 kg is equal to 220 pounds
"""

kg = 100
pound = kg * 2.2
print("{} kg is equal to {} pounds".format(kg, pound))
print(f"{kg} kg is equal to {pound:.0f} pounds")

