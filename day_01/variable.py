"""
Python is a dynamic language
Which means that it has dynamic typing
"""

# similar to null in java
name = None
print(name)

name = 'James'
print(name)

# to know the type of variable
print(type(name))

age = 33
print(type(age))

gpa = 3.5
print(type(gpa))

is_employed = False
print(type(is_employed))


s = '25'
s = int(s) # converting from string to int
print(s * 5)